function buildHistory(originalPrice, currentPrice) {
  const midpointOne = Math.round(originalPrice - (originalPrice - currentPrice) * 0.22);
  const midpointTwo = Math.round(originalPrice - (originalPrice - currentPrice) * 0.48);
  const midpointThree = Math.round(originalPrice - (originalPrice - currentPrice) * 0.78);

  return [
    { date: "2026-02-03", price: originalPrice },
    { date: "2026-02-14", price: midpointOne },
    { date: "2026-02-23", price: midpointTwo },
    { date: "2026-03-02", price: midpointThree },
    { date: "2026-03-07", price: currentPrice },
  ];
}

function createListing(config, index) {
  const { lat, lng } = areaMeta[config.area];
  const dropAmount = config.originalPrice - config.currentPrice;
  const dropPercent = (dropAmount / config.originalPrice) * 100;

  return {
    id: config.id,
    title: config.title,
    area: config.area,
    community: config.community,
    type: config.type,
    bedrooms: config.bedrooms,
    sqft: config.sqft,
    originalPrice: config.originalPrice,
    currentPrice: config.currentPrice,
    dropAmount,
    dropPercent: Number(dropPercent.toFixed(1)),
    daysOnMarket: config.daysOnMarket,
    droppedYesterday: config.droppedYesterday,
    validated: config.validated ?? true,
    expatArea: config.expatArea ?? true,
    familyFriendly: config.familyFriendly ?? false,
    lat,
    lng,
    priceHistory: buildHistory(config.originalPrice, config.currentPrice),
    lastScanned: config.lastScanned ?? scanTimes[index % scanTimes.length],
  };
}

