export function getAreaSummaries(inputListings = listings) {
  return Object.entries(areaMeta)
    .map(([area, details]) => {
      const areaListings = inputListings.filter((listing) => listing.area === area);
      const dropCount = areaListings.length;
      const avgDropPercent =
        dropCount === 0
          ? 0
          : areaListings.reduce((sum, listing) => sum + listing.dropPercent, 0) / dropCount;
      const avgDropAmount =
        dropCount === 0
          ? 0
          : areaListings.reduce((sum, listing) => sum + listing.dropAmount, 0) / dropCount;

      return {
        area,
        label: details.label,
        community: details.community,
        lat: details.lat,
        lng: details.lng,
        sparkline: details.sparkline,
        dropCount,
        avgDropPercent: Number(avgDropPercent.toFixed(1)),
        avgDropAmount: Math.round(avgDropAmount),
      };
    })
    .sort((left, right) => {
      if (right.dropCount !== left.dropCount) {
        return right.dropCount - left.dropCount;
      }

      return right.avgDropPercent - left.avgDropPercent;
    });
}

export function getDashboardStats(inputListings = listings) {
  const sortedByPercent = [...inputListings].sort((left, right) => right.dropPercent - left.dropPercent);
  const sortedByAmount = [...inputListings].sort((left, right) => right.dropAmount - left.dropAmount);
  const totalSavings = inputListings.reduce((sum, listing) => sum + listing.dropAmount, 0);

  return {
    totalDrops: inputListings.length,
    biggestDrop: sortedByPercent[0],
    biggestAmountDrop: sortedByAmount[0],
    totalSavings,
    averageDrop:
      inputListings.reduce((sum, listing) => sum + listing.dropPercent, 0) / Math.max(inputListings.length, 1),
  };
}
